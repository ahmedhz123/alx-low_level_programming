#include "3-calc.h"

/**
 * op_add - A function that returns the sum of two integers
 *
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: Integer
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that returns the subtraction of two integers
 *
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: An integer
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that returns the multiplication of two nums
 *
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: An integer
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that returns the division of two integers
 *
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: An integer
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that returns the mod of two nums
 *
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: An integer
*/

int op_mod(int a, int b)
{
	return (a % b);
}

